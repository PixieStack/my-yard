-- MyYard Township Import Script
-- Comprehensive list of 400+ South African townships organized by province

-- First, ensure the townships table exists with proper structure
CREATE TABLE IF NOT EXISTS townships (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(255) NOT NULL,
    municipality VARCHAR(255) NOT NULL,
    province VARCHAR(255) NOT NULL,
    city VARCHAR(255),
    type VARCHAR(50) DEFAULT 'township',
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create index for faster searches
CREATE INDEX IF NOT EXISTS idx_townships_name ON townships(name);
CREATE INDEX IF NOT EXISTS idx_townships_province ON townships(province);
CREATE INDEX IF NOT EXISTS idx_townships_municipality ON townships(municipality);

-- Clear existing townships to avoid duplicates
